#include "vector.h"

t_rgb	white(void)
{
	return ((t_rgb){
		.x = 0xFF,
		.y = 0xFF,
		.z = 0XFF,
	});
}

t_rgb	black(void)
{
	return ((t_rgb){
		.x = 0x0,
		.y = 0x0,
		.z = 0X0,
	});
}
